#ifndef MEMLAYOUT_H
#define   MEMLAYOUT_H
#include "param.h"

// qemu puts UART registers here in physical memory.
#define UART0 0x10000000L
#define UART0_IRQ 10

// virtio mmio interface
#define VIRTIO0 0x10001000
#define VIRTIO0_IRQ 1

// core local interruptor (CLINT), which contains the timer.
#define CLINT 0x2000000L
#define CLINT_MTIMECMP(hartid) (CLINT + 0x4000 + 8*(hartid))
#define CLINT_MTIME (CLINT + 0xBFF8) // cycles since boot.

#define CLINT_MSIP(hartid) (CLINT + 4*(hartid))

// qemu puts platform-level interrupt controller (PLIC) here.
// can be see at fu540-c000 manual
#define PLIC 0x0c000000L
#define PLIC_PRIORITY (PLIC + 0x0)
#define PLIC_PENDING (PLIC + 0x1000)
#define PLIC_MENABLE(hart) (PLIC + 0x2000 + (hart)*0x100)
#define PLIC_SENABLE(hart) (PLIC + 0x2080 + (hart)*0x100)
#define PLIC_MPRIORITY(hart) (PLIC + 0x200000 + (hart)*0x2000)
#define PLIC_SPRIORITY(hart) (PLIC + 0x201000 + (hart)*0x2000)
#define PLIC_MCLAIM(hart) (PLIC + 0x200004 + (hart)*0x2000)
#define PLIC_SCLAIM(hart) (PLIC + 0x201004 + (hart)*0x2000)

// the kernel expects there to be RAM
// for use by the kernel and user pages
// from physical address 0x80000000 to PHYSTOP.
#define KERNBASE 0x80000000L
#define PHYSTOP (KERNBASE + 128*1024*1024)

// map the trampoline page to the highest address,
// in both user and kernel space.
#define TRAMPOLINE (MAXVA - PGSIZE)

// map kernel stacks beneath the trampoline,
// each surrounded by invalid guard pages.
#define KSTACK(t) (TRAMPOLINE - ((t)+1)* 2*PGSIZE)
 //ustack=p->ustack_start+i*2*PGSIZE;

#define USTACK(start,i) ((start)+2*(i)*PGSIZE)
//(ustack-p->ustack_start)/PGSIZE;
#define USTACKI(start,ustack) (((ustack)-(start))/(2*PGSIZE))


// User memory layout.
// Address zero first:
//   text
//   original data and bss
//   fixed-size stack
//   expandable heap
//   ...
//   TRAPFRAME (p->trapframe, used by the trampoline)
//   TRAMPOLINE (the same page as in the kernel)
#define TRAPFRAME(i) (TRAMPOLINE - (i+1)*PGSIZE)
#define TRAPFRAMEI(va) (((TRAMPOLINE-(va))/PGSIZE)-1)
#define TRAPFRAME_LOW   TRAPFRAME(NPTHREAD-1)

//for memory map file
#define  NMMAP  64
// start map from this adress
#define  MMAPADDR (TRAPFRAME_LOW-NMMAP*PGSIZE)
#define  HEAPMAX   MMAPADDR
#endif



